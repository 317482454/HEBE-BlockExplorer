<template>
  <el-container>
    <fhead></fhead>
    <el-main >
      <el-card class="box-card center">
        <div slot="header" class="clearfix">
          <span>区块</span>
        </div>
        <el-table
                v-loading="BlocksLoading"
                  stripe
                  :data="BlocksData"
                  style="width: 100%">
          <el-table-column type="expand">
            <template slot-scope="scope">
              <el-table
                      :data="scope.row.transactions"
                      border
                      style="width: 100%">
                <el-table-column
                        label="发送地址"
                        width="280">
                  <template slot-scope="scope">
                    <router-link :to="'/account/'+scope.row.senderRS">
                      <el-button type="text"> {{scope.row.senderRS}}</el-button>
                    </router-link>
                  </template>
                </el-table-column>
                <el-table-column
                        label="接收地址"
                        width="280">
                  <template slot-scope="scope">
                    <router-link :to="'/account/'+scope.row.recipientRS">
                      <el-button type="text"> {{scope.row.recipientRS}}</el-button>
                    </router-link>
                  </template>
                </el-table-column>
                <el-table-column
                        label="数量"
                        width="180">
                  <template slot-scope="scope">
                    {{$g.wallet.amount(scope.row.amountNQT)}}
                  </template>
                </el-table-column>
                <el-table-column
                        label="手续费"
                        width="180">
                  <template slot-scope="scope">
                    {{$g.wallet.amount(scope.row.feeNQT)}}
                  </template>
                </el-table-column>
              </el-table>
            </template>
          </el-table-column>

          <el-table-column
                  label="高度"
                  width="180">
            <template  slot-scope="scope">
              <router-link :to="'block/'+scope.row.height">
                <el-button type="text"> {{scope.row.height}}</el-button>
              </router-link>
            </template>
          </el-table-column>
          <el-table-column
                  label="日期"
                  width="180">
            <template slot-scope="scope">
              {{$g.wallet.formatDateTime(scope.row.timestamp*1000+($store.state.epochBeginning-500))}}
            </template>
          </el-table-column>
          <el-table-column
                  label="转账数量"
                  width="180">
            <template slot-scope="scope">
              {{$g.wallet.amount(scope.row.totalAmountNQT)}}
            </template>
          </el-table-column>
          <el-table-column
                  label="交易费"
                  width="120">
            <template slot-scope="scope">
              {{$g.wallet.amount(scope.row.totalFeeNQT)}}
            </template>
          </el-table-column>
          <el-table-column
                  prop="transactions.length"
                  label="交易数"
                  width="80">
          </el-table-column>
          <el-table-column
                  label="出块者"
                  width="280">
            <template slot-scope="scope">
              <router-link :to="'/account/'+scope.row.generatorRS">
                <el-button type="text"> {{scope.row.generatorRS}}</el-button>
              </router-link>
            </template>
          </el-table-column>
        </el-table>
      </el-card>
      <el-card class="box-card center" style="margin-top: 60px">
        <div slot="header" class="clearfix">
          <span>锻造池</span>
          <span style="color: #909399;">（锻造人数：{{nextBlock.activeCount}}，非全部锻造人数）</span>
        </div>
        <el-table
                v-loading="nextBlockLoading"
                stripe
                  :data="nextBlock.generators"
                  style="width: 100%">
          <el-table-column type="expand">
            <template slot-scope="scope">
              <el-table
                      :data="scope.row.lessors"
                      border
                      style="width: 460px">
                <el-table-column
                        label="出租地址"
                        width="280">
                  <template slot-scope="scope">
                    <router-link :to="'/account/'+scope.row.lessorRS">
                      <el-button type="text"> {{scope.row.lessorRS}}</el-button>
                    </router-link>
                  </template>
                </el-table-column>
                <el-table-column
                        label="出租数量"
                        width="180">
                  <template slot-scope="scope">
                    {{$g.wallet.amount(scope.row.guaranteedBalanceNQT)}}
                  </template>
                </el-table-column>
              </el-table>
            </template>
          </el-table-column>
          <el-table-column
                  label="账户地址"
                  width="280">
            <template slot-scope="scope">
              <router-link :to="'/account/'+scope.row.accountRS">
                <el-button type="text"> {{scope.row.accountRS}}</el-button>
              </router-link>
            </template>
          </el-table-column>
          <el-table-column
                  label="锻造数量"
                  width="180">
            <template slot-scope="scope">
              <div v-if="scope.row.effectiveBalanceNXT">
                {{scope.row.effectiveBalanceNXT}}
              </div>
              <div v-if="scope.row.effectiveBalanceAPL">
                {{scope.row.effectiveBalanceAPL}}
              </div>
            </template>
          </el-table-column>
          <el-table-column
                  prop="sum"
                  label="出租人数量"
                  width="180">
          </el-table-column>
          <el-table-column
                  label="出块时间"
                  width="180">
            <template slot-scope="scope">
              {{$g.wallet.formatDateTime(scope.row.hitTime*1000+($store.state.epochBeginning-500))}}
            </template>
          </el-table-column>
          <el-table-column
                  prop="deadline"
                  label="出块倒计时间"
                  width="180">
          </el-table-column>

        </el-table>
      </el-card>
    </el-main>
    <el-footer class="footer">
